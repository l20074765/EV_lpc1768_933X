#ifndef __TARGET_CFG_H
#define __TARGET_CFG_H

/*********************************************************************************************************
  芯片加密级别定义
*********************************************************************************************************/
#ifdef DEBUG
#define CRP_LEVEL                   0                                   /*  0为禁止, 1~3依次提高加密级别*/
                                                                        /*  注意3级加密为OTP加密,加密后 */
                                                                        /*  芯片再也不能擦除            */
#endif                                                                  /*  DEBUG                       */

#ifdef RELEASE
#define CRP_LEVEL                   2                                   /*  0为禁止, 1~3依次提高加密级别*/
                                                                        /*  注意3级加密为OTP加密,加密后 */
                                                                        /*  芯片再也不能擦除            */
#endif                                                                  /*  RELEASE                     */

/*********************************************************************************************************
  系统时钟配置
*********************************************************************************************************/
#define FUSBCLK_EN                  1                                   /*  使能USB时钟                 */

#define FOSC                        12000000                            /*  振荡器频率                  */

#define FCCLK                      (FOSC  * 8)                          /*  主时钟频率<=100Mhz          */
                                                                        /*  FOSC的整数倍                */
#define FCCO                       (FCCLK * 3)                          /*  PLL频率(275Mhz~550Mhz)      */
                                                                        /*  与FCCLK相同，或是其的偶数倍 */
#define FPCLK                      (FCCLK / 4)                          /*  外设时钟频率,FCCLK的1/2、1/4*/
                                                                        /*  或与FCCLK相同               */

#define PLL_NVALUE                  1                                   /*  1~256                       */
#define PLL_MVALUE                ((FCCO / FOSC) * (PLL_NVALUE) / 2)    /*  6~512                       */

#if FUSBCLK_EN == 1
#define FUSBCLK                     48000000                            /*  必须为48000000              */
#endif                                                                  /*  USE_USB                     */

/*********************************************************************************************************
  RAM配置
*********************************************************************************************************/
#define RAM_TOP                    (0x10000000 + (32 * 1024))           /*  内部RAM的顶端               */

/*********************************************************************************************************
  异常向量表
*********************************************************************************************************/
#define VECTOR_TABLE_IN_FLASH       1                                   /*  异常向量表在FLASH的原始位置 */
#define MAX_VICS                    51                                  /*  异常向量表表项数目          */


#endif                                                                  /*  __TARGET_CFG_H              */

